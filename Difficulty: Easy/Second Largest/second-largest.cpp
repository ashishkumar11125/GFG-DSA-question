class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int l = -1, sl = -1;
        for(auto ele: arr) {
            if(ele > l) {
                sl = l;
                l = ele;
            } else if(ele > sl && l > ele) {
                sl = ele;
            }
        }
        return sl;
    }
};

