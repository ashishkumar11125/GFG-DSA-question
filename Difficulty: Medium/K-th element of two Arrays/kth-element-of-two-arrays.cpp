class Solution {
  public:
    int kthElement(vector<int>& a, vector<int>& b, int k) {
        // code here
        priority_queue<int,vector<int>,greater<int>> pq;
    for(int i=0;i<a.size();i++){
        pq.push(a[i]);
    }
    for(int i=0;i<b.size();i++){
        pq.push(b[i]);
    }
    while(k>1){
        pq.pop();
        k--;
    }
    return pq.top();
    }
};

